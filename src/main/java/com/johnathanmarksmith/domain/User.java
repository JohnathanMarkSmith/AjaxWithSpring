package com.johnathanmarksmith.domain;

public class User {
	
	private String name = null;
	private String phone = null;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}

    public String getPhone()
    {
        return phone;
    }

    public void setPhone(String phone)
    {
        this.phone = phone;
    }
}
